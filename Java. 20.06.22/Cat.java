package com.telran.berlin.prasice;

public class Cat {
    String breed;
    int age;
    String name;
    String color;

    public void setBreed(String text)  {
        breed = text;
    }
    public String getBreed() {
        return breed;
    }

    public void setAge(int text)  {
        age = text;
    }
    public int getAge() {
        return age;
    }

    public void setName(String text)  {
        name = text;
    }
    public String getName() {
        return name;
    }
    public void setColor(String text)  {
        color = text;
    }
    public String getColor() {
        return color;
    }

}
